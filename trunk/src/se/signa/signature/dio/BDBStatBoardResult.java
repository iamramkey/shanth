package se.signa.signature.dio;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class BDBStatBoardResult extends PositiveResult
{
	public String activeDeals;
	public String newDeals;
	public String volumeTrend;
	public String marginTrend;

	public BDBStatBoardResult()
	{
		super();
	}

	public BDBStatBoardResult(String notification, String activeDeals, String newDeals, String volumeTrend, String marginTrend)
	{
		super(notification);
		this.activeDeals = activeDeals;
		this.newDeals = newDeals;
		this.volumeTrend = volumeTrend;
		this.marginTrend = marginTrend;
	}
}
