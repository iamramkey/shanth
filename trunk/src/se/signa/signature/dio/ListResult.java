package se.signa.signature.dio;

import java.util.List;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class ListResult extends PositiveResult
{
	public List<String> list;

	public ListResult()
	{
		super();
	}

	public ListResult(String notification, List<String> list)
	{
		super(notification);
		this.list = list;
	}
}